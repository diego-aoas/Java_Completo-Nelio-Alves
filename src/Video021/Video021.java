package Video021;

public class Video021 {

    public static void main(String[] args) {

        // Exemplos de expressões aritméticas

        System.out.println("2 * 6 / 3           = " + 2 * 6 / 3);
        System.out.println("3 + 2 * 4           = " + (3 + 2 * 4));
        System.out.println("(3 + 2) * 4         = " + (3 + 2) * 4);
        System.out.println("60 / (3 + 2) * 4    = " + 60 / (3 + 2) * 4);
        System.out.println("60 / ((3 + 2) * 4)) = " + 60 / ((3 + 2) * 4));

    }

}
